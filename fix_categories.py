def categorize_product(title):
    title_lower = title.lower()
    
    # Check each category (shoes first to override clothing)
    for category, keywords in CATEGORY_RULES.items():
        for kw in keywords:
            if kw in title_lower:
                return category
    
    return 'clothing'  # default

# Count changes
changes = {}
for product in products:
    old_cat = product.get('category', 'unknown')
    new_cat = categorize_product(product.get('title', ''))
    
    if old_cat != new_cat:
        key = f"{old_cat} -> {new_cat}"
        changes[key] = changes.get(key, 0) + 1
        product['category'] = new_cat

# Print changes
print("=== Category Changes ===")
for change, count in sorted(changes.items(), key=lambda x: -x[1]):
    print(f"  {change}: {count}")

